# Calculadora de Gorjeta Inteligente
# O usuário insere o valor da conta e o nível de atendimento (ruim, médio, ótimo).
# O programa sugere uma gorjeta (exemplo: 5%, 10%, 15%).
# O programa deve validar a entrada do usuário e garantir que o valor da conta seja positivo e o nível de atendimento seja válido.
# O programa deve calcular e exibir o valor da gorjeta sugerida e o valor total a ser pago (conta + gorjeta).

# Detector de Promoções em Compras Online
# O usuário insere o preço de um produto e o preço original.
# O programa calcula o desconto e classifica a promoção:
# Acima de 50% → "Promoção incrível!"
# Entre 20% e 50% → "Boa promoção!"
# Menos de 20% → "Desconto pequeno, talvez não valha a pena."

# Calculadora de Tempo para Destino
# O usuário informa a distância (km) e o meio de transporte (carro, bicicleta, a pé).
# O programa calcula e informa um tempo estimado de chegada.

# Planejador de Roupa do Dia (Baseado no Clima)
# O usuário informa a temperatura atual.
# O programa sugere uma roupa:
# Se for abaixo de 15°C → "Vista um casaco pesado!"
# Se for entre 15°C e 25°C → "Use uma blusa leve!"
# Se for acima de 25°C → "Vá de camiseta!"

# Gerador de Respostas para Mensagens Chatas
# O usuário insere um tipo de mensagem que recebeu (ex: cobrança de algo, convite indesejado).
# O programa sugere respostas educadas, mas evasivas.

# Detector de Fraudes em Boletos
# O usuário insere o código de barras de um boleto.
# O programa verifica se ele tem o tamanho correto e sugere atenção caso falte números.

# Calculadora de Custo de Viagem
# O usuário informa a distância e o consumo do carro em km/L.
# O programa calcula quanto custaria a viagem com base no preço médio do combustível.

# Lembrete de Hidratação
# O usuário informa quantos copos de água bebeu no dia.
# O programa recomenda beber mais água, se necessário.